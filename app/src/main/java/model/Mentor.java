public class Mentor
{
    private String mentorQes;
    private String mentorQesId;
    private String rollno;

    public String getMentorQes() {
        return mentorQes;
    }

    public void setMentorQes(String mentorQes) {
        this.mentorQes = mentorQes;
    }


    public String getMentorQesId() {
        return mentorQesId;
    }

    public void setMentorQesId(String mentorQesId) {
        this.mentorQesId = mentorQesId;
    }


    public String getUserId() {
        return rollno;
    }

    public void setUserId(String rollno) {
        this.rollno = rollno;
    }
}
